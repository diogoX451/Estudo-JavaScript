/**
 * Todos os operadores de comparação são:
 * == (igual)
 * != (different) // somente valor
 * < (menor)
 * > (maior)
 * <= (menor ou igual)
 * >= (maior ou igual)
 * === (identico) // Ver valor e tipo
 * !== (diferente estrito) // Valor e tipo
 */

const number = 1;
const number2 = 1;
console.log(number != number2); // retorna true

//Operadores logicos
/**
 * && -> AND
 * || -> OR
 * ! -> NOT
 */
console.log(true && true); // retorna true, todas expressões devem ser verdadeiras
console.log(true && false); // retorna false
console.log(true || false); // retorna true, depende se uma for true ou false, que retorna true
